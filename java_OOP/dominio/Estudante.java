package java_OOP.dominio;

public class Estudante {
    public String nome;
    public int idade;
    public char sexo;

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getIdade() {
        return idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }
    public char getSexo() {
        return sexo;
    }
    public void setSexo(char Sexo) {
        this.sexo = sexo;
    }
}
